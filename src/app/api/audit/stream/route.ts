// ─── Supabase save helpers ───────────────────────────────────

async function ensureAgent(
  supabase: any,
  userId: string,
  agentName: string,
  repoUrl: string | null,
  toolList: DetectedAgentTool[]
): Promise<string | null> {
  const { data: existing } = await supabase
    .from("agents")
    .select("id")
    .eq("user_id", userId)
    .eq("name", agentName)
    .eq("repo_url", repoUrl ?? "")
    .maybeSingle();

  if (existing) return existing.id;

  const { data: inserted } = await supabase
    .from("agents")
    .insert({
      user_id: userId,
      name: agentName,
      repo_url: repoUrl ?? "",
      config: { tools: toolList },
    })
    .select("id")
    .single();

  return inserted?.id ?? null;
}

async function saveVulnerabilities(
  supabase: any,
  reportId: string,
  vulnerabilities: Vulnerability[]
): Promise<{ idMap: Record<string, string>; counts: Record<string, number> }> {
  const idMap: Record<string, string> = {};
  const counts = { vulnerability_count: 0, critical_count: 0, high_count: 0, medium_count: 0, low_count: 0 };

  // Track categories of vulnerabilities that the LLM detected but our catalogue
  // does not know about. We surface this as a warning rather than silently
  // dropping the row, and we skip the insert so the database FK is obeyed.
  const skippedCategories = new Set<string>();

  for (const vuln of vulnerabilities) {
    const { data: inserted, error } = await supabase
      .from("vulnerabilities")
      .insert({
        report_id: reportId,
        category_id: vuln.category_id,
        title: vuln.title,
        severity: vuln.severity,
        description: vuln.description,
        remediation: vuln.remediation,
        file_path: vuln.file_path,
        line_number: vuln.line_number,
        code_snippet: vuln.code_snippet,
        root_cause: vuln.root_cause,
        attack_path: vuln.attack_path,
        impact: vuln.impact,
        cvss_score: vuln.cvss_score,
        status: vuln.status,
      })
      .select("id")
      .single();

    if (error) {
      console.warn(
        `[saveVulnerabilities] failed to insert vuln category_id="${vuln.category_id}" ` +
          `title="${vuln.title?.slice(0, 60)}": ${error.message} (code ${error.code})`,
      );
      if (error.code === "23503") {
        // FK violation — vulnerability_categories does not have this id.
        skippedCategories.add(vuln.category_id);
      }
      // For FK mismatch, skip silently for the index map but still count it in
      // the final summary so the user sees what was discarded.
      continue;
    }
    if (inserted) {
      idMap[vuln.id] = inserted.id;
    }
    counts.vulnerability_count++;
    if (vuln.severity === "critical") counts.critical_count++;
    else if (vuln.severity === "high") counts.high_count++;
    else if (vuln.severity === "medium") counts.medium_count++;
    else if (vuln.severity === "low") counts.low_count++;
  }

  if (skippedCategories.size > 0) {
    console.warn(
      `[saveVulnerabilities] ${skippedCategories.size} LLM-detected categories were ` +
        `not in vulnerability_categories and were skipped: ${Array.from(skippedCategories).join(", ")}`,
    );
  }

  return { idMap, counts };
}

async function saveSimulationTurns(
  supabase: any,
  reportId: string,
  turns: SimulationTurn[],
  vulnIdMap: Record<string, string>
): Promise<{ compromisedCount: number; insertedCount: number; error?: string }> {
  if (turns.length === 0) {
    return { compromisedCount: 0, insertedCount: 0 };
  }

  let compromisedCount = 0;
  const rows = turns.map((turn) => {
    if (turn.compromised) compromisedCount++;
    const mappedVulnId = turn.target_vuln_id ? (vulnIdMap[turn.target_vuln_id] ?? null) : null;
    return {
      report_id: reportId,
      turn_number: turn.turn_number,
      sender: turn.sender,
      text: turn.text,
      target_vuln_id: mappedVulnId,
      compromised: turn.compromised ?? false,
      session_category: turn.session_category ?? null,
    };
  });

  // Single batched insert. The previous per-row `await` loop silently
  // dropped any failed row — Postgres RLS / FK errors were never surfaced,
  // so the caller believed the rows existed when they didn't. The very
  // recent `effective_severity = "low"` across-the-board bug was caused by
  // an upper-layer orchestrator crash (TDZ) that prevented this loop from
  // running at all; even when it runs, the silent-drop failure mode would
  // have the same downstream effect (no PoC → all low). We now surface the
  // error to the caller and they decide whether to mark the audit as
  // degraded in `audit_reports`.
  const { error } = await supabase.from("simulation_turns").insert(rows);
  if (error) {
    return {
      compromisedCount,
      insertedCount: 0,
      error: `${error.message} (code ${error.code})`,
    };
  }

  return { compromisedCount, insertedCount: rows.length };
}

